    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        HiloResultados resultados1 = new HiloResultados(new LoteriaNacional());
        HiloResultados resultados2 = new HiloResultados(new Lotto());
        HiloResultados resultados3 = new HiloResultados(new TresMonasos());
        HiloResultados resultados4 = new HiloResultados(new Tiempos());
        resultados1.start();
        resultados2.start();
        resultados3.start();
        resultados4.start();
        try {
            resultados1.join();
            resultados2.join();
            resultados3.join();
            resultados4.join();
            JOptionPane.showMessageDialog(this, "¬°Se publicaron todos los resultados!");
        } catch (InterruptedException ex) {
            Logger.getLogger(AdministradorInicio.class.getName()).log(Level.SEVERE, null, ex);
        }               
    }//GEN-LAST:event_jButton7ActionPerformed

